#tipe data : angka satuan(integer)
data_int =4
# untuk mengetahui tipe data dalam suatu variabel menggunkana func type
print("nilai: ",data_int,"tipe data: ",type(data_int))

# tipe data :decimal (float)
data_float = 0.4
print("nilai: ",data_float,"tipe data: ",type(data_float))

#tipe data : kumpulan karakter (string)
data_str = "0.4"
print("nilai: ",data_str,"tipe data: ",type(data_str))

#tipe data : boolean true/false
data_bool = True
print("nilai: ",data_bool,"tipe data: ",type(data_bool))
